"""## Cell 11: Main Evaluation Loop

For each sample:
1. Extract context (1 call to Qwen)
2. Run action inference K=3 times (3 calls to Qwen)
3. Apply safety rules to each run
4. Majority vote → final action
5. Record everything

"""

