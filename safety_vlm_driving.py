"""## Cell 9: Safety Rules Layer (Tiers 1–4)
This is the **core contribution** of the project.
Given a predicted action + extracted context, apply hierarchical safety rules.
"""

def parse_context(context_dict: dict) -> dict:
    """
    Normalize context values to booleans / strings for rule checking.
    Handles minor variations in model output (e.g. 'yes.', 'Yes', 'RED').
    """
    def is_yes(val):
        return str(val).lower().strip().rstrip('.') in {'yes', 'true', '1'}

    def is_no(val):
        return str(val).lower().strip().rstrip('.') in {'no', 'false', '0', 'none'}

    tl = str(context_dict.get('traffic_light', 'none')).lower().strip().rstrip('.')

    return {
        'traffic_light':       tl,              # 'red','yellow','green','none'
        'stop_sign':           is_yes(context_dict.get('stop_sign', 'no')),
        'crosswalk':           is_yes(context_dict.get('crosswalk', 'no')),
        'pedestrian':          is_yes(context_dict.get('pedestrian', 'no')),
        'vehicle_ahead':       is_yes(context_dict.get('vehicle_ahead', 'no')),
        'vehicle_behind':      is_yes(context_dict.get('vehicle_behind', 'no')),
        'lane_blocked':        is_yes(context_dict.get('lane_blocked', 'no')),
        'drivable_left':       not is_no(context_dict.get('drivable_left', 'yes')),
        'drivable_right':      not is_no(context_dict.get('drivable_right', 'yes')),
        'visibility_degraded': is_yes(context_dict.get('visibility_degraded', 'no')),
    }


def apply_safety_rules(action: str, context_raw: dict) -> tuple:
    """
    Apply 4-tier safety rules to the predicted action.

    Returns:
        (safe_action, fired_rules, was_overridden)

    Tiers:
      Tier 1 – Mandatory Stop conditions
      Tier 2 – Lateral (lane change) legality
      Tier 3 – Rear-end risk
      Tier 4 – Visibility
    """
    c = parse_context(context_raw)
    fired = []   # list of rule IDs that fired

    # Build feasible action set — start with all actions, remove as rules fire
    feasible = set(ACTION_SPACE) - {'unknown'}

    # ── Tier 1: Mandatory Stop ────────────────────────────────────────────────
    # R1: Red light or stop sign → only stop-compatible actions allowed
    if c['traffic_light'] == 'red' or c['stop_sign']:
        fired.append('R1')
        feasible = feasible & {'stop', 'brake hard', 'brake gently'}

    # R2: Yellow light → no accelerating
    if c['traffic_light'] == 'yellow':
        fired.append('R2_yellow')
        feasible.discard('accelerate')

    # R3: Pedestrian or cyclist in road → ACCELERATE disallowed
    if c['pedestrian']:
        fired.append('R3')
        feasible.discard('accelerate')
        # Also disallow lane changes toward them if crosswalk present
        if c['crosswalk']:
            fired.append('R3_crosswalk')
            feasible.discard('change lane left')
            feasible.discard('change lane right')

    # R4: Lane blocked ahead → prefer braking
    if c['lane_blocked']:
        fired.append('R4')
        feasible.discard('accelerate')
        feasible.discard('keep speed')

    # ── Tier 2: Lateral Legality ──────────────────────────────────────────────
    # R5: No drivable space left → prohibit left lane change
    if not c['drivable_left']:
        fired.append('R5_left')
        feasible.discard('change lane left')

    # R6: No drivable space right → prohibit right lane change
    if not c['drivable_right']:
        fired.append('R5_right')
        feasible.discard('change lane right')

    # ── Tier 3: Rear-End Risk ─────────────────────────────────────────────────
    # R7: Vehicle behind close → avoid abrupt deceleration
    if c['vehicle_behind']:
        fired.append('R7')
        feasible.discard('brake hard')
        feasible.discard('stop')   # sudden stop dangerous with tailgater

    # ── Tier 4: Visibility ────────────────────────────────────────────────────
    # R8: Degraded visibility → no aggressive actions
    if c['visibility_degraded']:
        fired.append('R8')
        feasible.discard('accelerate')
        feasible.discard('change lane left')
        feasible.discard('change lane right')
        feasible.discard('brake hard')   # ADD THIS LINE
    # ── Check if original action is feasible ─────────────────────────────────
    if not feasible:
        # Edge case: all actions blocked — use stop as ultimate fallback
        # (if R7 fired, use brake gently instead)
        fallback = 'brake gently' if 'R7' in fired else 'stop'
        return fallback, fired, True

    if action in feasible:
        # Original action is safe — no override needed
        return action, fired, False
    else:
        # Override: pick safest action from feasible set
        for conservative_action in CONSERVATIVE_ORDER:
            if conservative_action in feasible:
                return conservative_action, fired, True
        # Fallback (should not reach here)
        return list(feasible)[0], fired, True


def is_unsafe(action: str, context_raw: dict) -> bool:
    """
    Check if an action violates any active safety rule.
    Returns True if the action is unsafe.
    """
    safe_action, fired_rules, was_overridden = apply_safety_rules(action, context_raw)
    return was_overridden


