"""## Cell 10: Consistency Function
Run each sample K=3 times, apply safety rules to each, then majority vote.
"""

def consistency_vote(safe_actions: list) -> tuple:
    """
    Given K refined actions, pick the majority vote.
    If tie: pick the more conservative one.

    Returns:
        (final_action, is_consistent)
        is_consistent = True if majority agreement (>50%)
    """
    counts = Counter(safe_actions)
    most_common_action, most_common_count = counts.most_common(1)[0]
    K = len(safe_actions)

    # Majority = more than half
    is_consistent = most_common_count > K / 2

    if is_consistent:
        return most_common_action, True
    else:
        # Tie: pick most conservative from the tied actions
        tied_actions = [a for a, c in counts.items() if c == most_common_count]
        for conservative_action in CONSERVATIVE_ORDER:
            if conservative_action in tied_actions:
                return conservative_action, False
        return most_common_action, False

