# ── Core inference function ───────────────────────────────────────────────────

def run_qwen(image_path: str, system_prompt: str, user_text: str,
             temperature: float = 0.2, max_new_tokens: int = 256) -> str:
    """
    Run Qwen2-VL on a single image + text prompt.
    Returns raw string output.
    """
    image_uri = "file://" + os.path.abspath(image_path)

    messages = [
        {"role": "system", "content": system_prompt},
        {"role": "user",   "content": [
            {"type": "image", "image": image_uri},
            {"type": "text",  "text":  user_text},
        ]},
    ]

    text_input  = qwen_processor.apply_chat_template(
        messages, tokenize=False, add_generation_prompt=True
    )
    image_inputs, video_inputs = process_vision_info(messages)

    inputs = qwen_processor(
        text=[text_input],
        images=image_inputs,
        videos=video_inputs,
        return_tensors="pt"
    )
    inputs = {k: v.to("cuda") for k, v in inputs.items() if hasattr(v, "to")}

    with torch.no_grad():
        gen = qwen_model.generate(
            **inputs,
            max_new_tokens=max_new_tokens,
            temperature=temperature,
            top_p=0.9,
            do_sample=(temperature > 0)
        )

    out_tokens = gen[:, inputs["input_ids"].shape[1]:]
    return qwen_processor.batch_decode(out_tokens, skip_special_tokens=True)[0]


def parse_json_output(raw: str) -> dict:
    """Robustly extract the first JSON dict from a model response."""
    raw = raw.strip()
    # Try direct parse first
    try:
        return json.loads(raw)
    except Exception:
        pass
    # Try to find JSON block inside markdown fences or extra text
    match = re.search(r'\{[^{}]+\}', raw, re.DOTALL)
    if match:
        try:
            return json.loads(match.group())
        except Exception:
            pass
    return {"parse_error": True, "raw": raw}


